# 	length of the list
# 	the 5th item in the list
# 	the first four items in the list in a sub list.
# 	the last three items in the list.

hobbies = ["playing squash", "programming", "reading", "petting cats", "playing golf", "visiting friends", "playing video games", "playing board games", "watching movies"]

print(f"The length of the list is {len(hobbies)}")
print(f"The 5th item in the list is {hobbies[4]}")
print(f"The first four items in the list are {hobbies[0:4]}")
print(f"The last three items in the list are: {hobbies[-3:]}")
print(f"The last three items in the list are: {hobbies[6:10]}")

